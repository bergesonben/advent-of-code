async function p2022day10_part1(input: string, ...params: any[]) {
	let x = 1;
	let currCycle = 1;
	let sum = 0;
	const offset = 20;
	const lines = input.split("\n");
	for (const line of lines) {	
		execute(line);	
	}
	return sum;

	function execute(line: string): void {
		if (line.trim() == 'noop') {
			incrementCycle();
		} else {
			const yeet = line.split(' ');
			add(Number(yeet[1]));
		}
	}

	function add(num: number): void {
		incrementCycle();
		incrementCycle();
		x += num;
	}

	function incrementCycle(): void {
		if ((currCycle - offset) % 40 == 0) {
			sum += x * currCycle;
		}
		currCycle++;
	}
}

async function p2022day10_part2(input: string, ...params: any[]) {
	let x = 1;
	let currCycle = 1;	
	let screen = '';
	const lines = input.split("\n");
	for (const line of lines) {	
		execute(line);	
	}
	return screen;

	function execute(line: string): void {
		if (line.trim() == 'noop') {
			incrementCycle();
		} else {
			const yeet = line.split(' ');
			add(Number(yeet[1]));
		}
	}

	function add(num: number): void {
		incrementCycle();
		incrementCycle();
		x += num;
	}

	function incrementCycle(): void {
		if (isSpriteVisible()) {
			screen += '#'
		} else {
			screen += '.'
		}
		if (getPixelPos() == 39) {
			screen += '\n'
		}
		currCycle++;
	}

	function isSpriteVisible(): boolean {
		const currPixel = getPixelPos();
		return x-1 <= currPixel && currPixel <= x+1;
	}

	function getPixelPos(): number {
		return (currCycle-1) % 40;
	}
}

